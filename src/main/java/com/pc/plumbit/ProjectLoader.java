    public ProjectLoader() {
        initComponents();
        DataInitializer.setTableStyling(projectListTable);
        this.setLocation(Window.WIDTH/2, Window.HEIGHT/2);
        String location = "D:\\Programming\\GUI\\NB\\PlumbIT\\pdf";
        Path dir = Paths.get(location);
        try (DirectoryStream<Path> stream = Files.newDirectoryStream(dir, "*.pscs")) {
            for (Path entry : stream) {
                projectList.put(entry.getFileName().toString(), entry.toString());
            }
            if(!projectList.isEmpty()) {
                DefaultTableModel tableModel = (DefaultTableModel) projectListTable.getModel();
                for(String key : projectList.keySet()) {
                    tableModel.addRow(new Object[]{
                        key, projectList.get(key)
                    });
                }
                
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Unable to load saved project", "Error", JOptionPane.ERROR);
            log.error("Unable to read project folder", e);
        }
    }
    

