    private void projectListTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_projectListTableMouseClicked
        if (evt.getClickCount() == 2 && evt.getButton() == MouseEvent.BUTTON1) {
            int row = projectListTable.rowAtPoint(evt.getPoint());
            if (row >= 0) {
                String projetPath = (String)projectListTable.getValueAt(row, 1);
                try{
                    XStream xstream = DataInitializer.getXstream();
                    File file = new File(projetPath);
                    PdfData projectData = (PdfData) xstream.fromXML(file);
                    DataCalculator dc = new DataCalculator(projectData);
                    dc.setVisible(true);
                } catch(Exception e) {
                    JOptionPane.showMessageDialog(this, "Unable to load saved project", "Error", JOptionPane.ERROR);
                    log.error("failed to load project",e);
                }
            }
        }
    }//GEN-LAST:event_projectListTableMouseClicked

